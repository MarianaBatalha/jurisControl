// --- Date Helper ---
// Creates dates relative to today to make the mock data feel current.
const getPastDate = (monthsAgo: number, daysAgo = 0): string => {
    const date = new Date();
    date.setMonth(date.getMonth() - monthsAgo);
    date.setDate(date.getDate() - daysAgo);
    return date.toISOString().split('T')[0];
};


